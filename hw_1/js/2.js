/*2. Создать скрипт, получающий два значения и выводящий true, если значения равны, false - если нет*/
let firstValue = prompt("Введите число!");
let secondValue = prompt("Введите еще одно число!");

if (firstValue === secondValue) {
    console.log('значения равны: ', true);
} else {
    console.log('значения не равны: ', false);
}

